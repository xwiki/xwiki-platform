<!-- BooleanFilter is a custom filter that allow to filter boolean values. -->
<template>
  <select :value="filterValue" class="xwiki-selectize livedata-selectize" ref="input">
    <option value=""></option>
    <option :value="getTrueValue()">$t('livedata.filter.boolean.yes')</option>
    <option :value="getFalseValue()">$('livedata.filter.boolean.no')</option>
  </select>
</template>


<script>
import filterMixin from "./filterMixin.js";
import $ from "jquery";

export default {
  name: "filter-boolean",

  // Add the filterMixin to get access to all the filters methods and computed properties inside this component
  mixins: [filterMixin],

  data() {
    return {
      filterValue: undefined
    }
  },

  methods: {
    getTrueValue()
    {
      if (this.propertyId === 'doc.hidden') {
        return 'true';
      } else {
        return '1'
      }
    },
    getFalseValue()
    {
      if (this.propertyId === 'doc.hidden') {
        return 'false';
      } else {
        return '0'
      }
    }
  },

  watch: {
    filterValue(newValue, oldValue) {
      if (newValue !== oldValue) {
        $(this.$refs.input).val(newValue).trigger('change');
        this.applyFilter(newValue);
      }
    },
  },

  // Create the selectize widget.
  mounted() {
    $(this.$refs.input).xwikiSelectize({
      onChange: value => {
        this.filterValue = value;
      },
    });
  },

  // Destroy the selectize widget.
  beforeDestroyed() {
    this.$refs.input.selectize?.destroy();
  },

}
</script>

<style>
.livedata-selectize.xwiki-selectize {
  margin-top: 6px;
}
</style>